package mx.mc.model;

import java.util.Date;

/**
 *
 * @author apalacios
 */
public class DispensacionMaterialInsumoExtended extends DispensacionMaterialInsumo {  

    private static final long serialVersionUID = 1L;
    private String claveInstitucional;
    private String nombreCorto;
    private String lote;
    private Date fechaCaducidad;
    
    public DispensacionMaterialInsumoExtended() {
        //No code needed in constructor
    }

    public String getLote() {
        return lote;
    }

    public void setLote(String lote) {
        this.lote = lote;
    }

    public String getClaveInstitucional() {
        return claveInstitucional;
    }

    public void setClaveInstitucional(String claveInstitucional) {
        this.claveInstitucional = claveInstitucional;
    }

    public Date getFechaCaducidad() {
        return fechaCaducidad;
    }

    public void setFechaCaducidad(Date fechaCaducidad) {
        this.fechaCaducidad = fechaCaducidad;
    }

    public String getNombreCorto() {
        return nombreCorto;
    }

    public void setNombreCorto(String nombreCorto) {
        this.nombreCorto = nombreCorto;
    }
    
}
